import mongoose from "mongoose";
import NotFoundError from "./errors/not-found";

const { Schema } = mongoose;

const userSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

export default userSchema;
export { NotFoundError };
